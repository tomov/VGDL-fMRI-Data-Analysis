function [masks, region] = get_masks_from_study(tag, sphere)

    % create spherical masks around peak ROIs from contrast
    % copied from Exploration

    if ~exist('sphere', 'var')
        sphere = 10; % 10 mm radius by default
    end

    EXPT = vgdl_expt();

    switch tag
        case 'tomov2018KL'

            mni = [ ...
                44 12 50; ...
                56 -60 30; ...
                -38 26 26; ...
                24 64 -14; ...
                -40 56 6; ...
                -40 -62 -46; ...
                2 38 48; ...
                62 -44 -12; ...
                -34 -66 46; ...
                -28 -72 -28; ...
                -46 -28 20; ...
                -24 32 -10; ...
                -18 -48 60];

        case 'tomov2018RSA'

            mni = [ ...
                34 -58 42; ...
                -50 -58 -18; ...
                -46 -38 44; ...
                -46 -20 6; ...
                44 20 -6; ...
                -56 26 24];

        case 'hayley2021psi'

            mni = [ ...
                42    24    24; ...
                36    16    6];

        case 'pilot21'
            mni = [48 12 30];

        otherwise
            assert(false, 'invalid tag');

    end

    masks = create_masks_from_mni(tag, mni, true, sphere);

    for c = 1:length(masks)
        mask = masks{c};
        [~, masknames{c}, ~] = fileparts(mask);
        region{c,:} = masknames{c};
    end
